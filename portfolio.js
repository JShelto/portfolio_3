//make sure the lower links are visible as the user scrolls
$(window).scroll(function (event) {
  var scroll = $(window).scrollTop();
  elements = $("a.lower-link");
  for (var i = 0; i < elements.length; i++) {
    if(scroll >= 66 && scroll <= 104) {
      elements[i].style.color = "#8E949B";
    } else if(scroll >= 180 && scroll <= 210) {
      elements[i].style.color = "#8E949B";
    } else if(scroll >= 234 && scroll <= 275) {
      elements[i].style.color = "#8E949B";
    } else if(scroll >= 302 && scroll <= 342) {
      elements[i].style.color = "#8E949B";
    } else if(scroll >= 365 && scroll <= 416) {
      elements[i].style.color = "#8E949B";
    } else {
      elements[i].style.color = "#FFFFFF";
    }
  }
  console.log(scroll);
});